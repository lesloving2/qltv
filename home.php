<p>Linh hồn có thực hay không? Thế giới sau cái chết có tồn tại hay không? Đó là một câu hỏi mà con người thường có xu hướng không tin và không công nhận nếu họ chưa từng trải nghiệm hay tự mình chứng kiến tận mắt trong cuộc đời. Thế nhưng những điều kỳ bí luôn hiện hữu, và có những con người mang khả năng đặc biệt xuất hiện để minh chứng cho những điều tưởng chừng không có thực đó. Người phụ nữ “ba mắt” – nhà ngoại cảm Hoàng Thị Thiêm chính là một trong những con người ấy.

Sinh ra ở Hải Phòng rồi cùng gia đình chuyển lên vùng núi Lào Cai, dù thuộc thế hệ 7x nhưng cô đã tận mắt chứng kiến những đau thương, tàn khốc của chiến tranh trong cuộc chiến tranh biên giới. Ngay từ nhỏ, cô đã có những khả năng kỳ lạ và những cuộc gặp gỡ trong mơ, và cả khi tỉnh táo, với những người ở cõi khác, nhưng cô chưa để ý nhiều tới điều này. Đến tuổi trưởng thành, khi đã yên bề gia thất, cô phải trải qua những năm tháng bị căn bệnh kỳ lạ hành hạ. Thời gian đó, ai cũng tưởng cô bị điên. Kỳ thực, đó là lúc cô được bề trên dạy cho những điều về cõi giới bên kia, được hướng dẫn cho kỹ thuật áp vong để người hai cõi Âm – Dương có thể gặp gỡ, trò chuyện
</p>
<p>
Và hành trình kết nối giữa hai thế giới ấy đã được kể lại trong cuốn tự truyện “Trò chuyện với cõi vô hình”. Sự ra đời của cuốn sách cũng là một câu chuyện thú vị như chính hành trình khám phá và kết nối tâm linh của tác giả – nhà ngoại cảm Hoàng Thị Thiêm. Cuốn sách được một công ty sách xuất bản đầu tiên vào năm 2017 và đã được đông đảo bạn đọc trên cả nước ủng hộ. Những câu chuyện đầy kỳ bí về một thế giới vô hình của Hoàng Thị Thiêm đã lôi cuốn, thu hút và giúp người đọc có cái nhìn đa chiều và sâu sắc hơn về cuộc sống hiện tại, cuộc sống trong quá khứ cũng như cuộc sống ở một kiếp nào đó vô hình.

